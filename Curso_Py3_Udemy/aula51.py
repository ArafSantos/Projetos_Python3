'''
📌 1. Mutabilidade
Lista: Mutável – os elementos podem ser alterados, adicionados ou removidos após a criação.
Tupla: Imutável – os elementos não podem ser modificados depois de criada.

📌 2. Sintaxe
Lista: Definida com colchetes [] → lista = [1, 2, 3]
Tupla: Definida com parênteses () → tupla = (1, 2, 3)

📌 3. Desempenho
Listas são mais lentas porque precisam permitir modificações.
Tuplas são mais rápidas porque são imutáveis e ocupam menos memória.

📌 4. Uso Ideal
Lista: Quando os dados precisam ser alterados (adicionados, removidos ou modificados).
Tupla: Quando os dados devem permanecer fixos e seguros, garantindo que nada será alterado acidentalmente.

📌 5. Métodos Disponíveis
Listas possuem vários métodos como .append(), .remove(), .pop(), .sort(), etc.
Tuplas têm apenas dois métodos principais: .count() e .index().
🏆 Resumo Final
Use listas quando precisar de flexibilidade e tuplas quando precisar de segurança e desempenho.

Se precisar de exemplos práticos, me avise! 🚀
'''